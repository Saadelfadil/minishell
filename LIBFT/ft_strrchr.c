#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = -1;
	while (s[++i])
		;
	while (i >= 0)
		if (s[i] == (char)c)
			return ((char*)s + i);
		else
			i--;
	return (NULL);
}
